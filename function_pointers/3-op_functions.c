#include "3-calc.h"
/**
 *op_add - addition
 *@a: int
 *@b: int
 *Return: 0
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 *op_sub - soustraction
 *@a: int
 *@b: int
 *Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply
 * @a: int
 * @b: int
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: int
 * @b: int
 * Return: Always 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo
 * @a: int
 * @b: int
 * Return: Always 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
